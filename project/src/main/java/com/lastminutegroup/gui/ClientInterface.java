    public ClientInterface() throws FileNotFoundException, IOException {

        initComponents();
        FileInputStream in = new FileInputStream("/Users/Lucci/NetBeansProjects/project/src/DB/Database.xls");
        POIFSFileSystem fs = new POIFSFileSystem(in);
        HSSFWorkbook wb = new HSSFWorkbook(fs);
        HSSFSheet sheet = wb.getSheetAt(0);
        HSSFRow row;
        HSSFCell cell;

        int rows;
        int product_ID;
        String name;
        double price;
        String description;
        int quantity;
        String type;
        String imported;

        rows = sheet.getPhysicalNumberOfRows();

        db = new ArrayList<Product>();
        SelectedProduct = new ListOfProduct();
        Basket = new ListOfProduct();

        for (int i = 1; i < rows; i++) {

            product_ID = (int) sheet.getRow(i).getCell(0).getNumericCellValue();
            name = sheet.getRow(i).getCell(1).getStringCellValue();
            price = Double.parseDouble(sheet.getRow(i).getCell(2).getStringCellValue());
            description = sheet.getRow(i).getCell(3).getStringCellValue();
            type = sheet.getRow(i).getCell(4).getStringCellValue();
            imported = sheet.getRow(i).getCell(5).getStringCellValue();

            db.add(EvaluateProduct.evaluateProduct(product_ID, name, price, description, 1, type, imported));

            //System.out.println("  " + sheet.getRow(0).getCell(j) + ": " + sheet.getRow(i).getCell(j));
        }

        for (int i = 0; i < rows - 1; i++) {
            SelectedProduct.addToBasket(db.get(i).getProduct_ID() + "-" + db.get(i).getName() + " " + db.get(i).getPrice());
            SelectedProduct.addToProductIDs(String.valueOf(db.get(i).getProduct_ID()));
        }

        ListSelectedProduct = new JList(makeStrings(SelectedProduct.getBasket()));
        ListSelectedProduct.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
        ListSelectedProduct.setSelectedIndex(0);
        jScrollPane2.setViewportView(ListSelectedProduct);
        jButton2.setVisible(false);
        jButton4.setVisible(false);

    }

